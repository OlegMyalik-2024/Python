#Генерируем список случайных целых чисел
print("-------------------------------Задание 1 (Вариант 4)-----------------------------------------------------")
list1 = []
k = int(input("Введите количество элементов в списке: "))
for i in range(k):
    list1.append(random.randint(-5, 10))
print("Исходный список = ",list1)
#Находим произведение элементов списка с нечетными номерами.
product = 1
for i in range(0, len(list1), 2):
    product *= list1[i]
print("Произведение элементов списка = ", product)
#Находим наибольший элемент списка, затем удаляем его и выводим новый список
max_element=max(list1)
print("Максимальный элемент списка = ", max_element)
list1.remove(max_element)
#Удаляем максимальный элемент в списке если он повторяется
result = []
for i in list1:
    if i==max_element:
        list1.remove(i)
        result.append(i)
print("Новый список после удаления максимального элемента:", list1)
print("---------------------------------------------------------------------------------------------------------")
