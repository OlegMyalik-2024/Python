rand_list = []
n = int(input("Введите количество элементов в списке: "))
for i in range(n):
    rand_list.append(random.randint(-5, 10))
print("Исходный список = ",rand_list)
# Находим наибольший нечетный элемент в списке
mx2 = max( x for x in rand_list if x%2!=0)
print("Наибольший нечетный элемент = ", mx2)
# Находим минимальный по модулю элемент списка.
min1=abs(min(rand_list))
print("Минимальный по модулю элемент списка = ", min1)
